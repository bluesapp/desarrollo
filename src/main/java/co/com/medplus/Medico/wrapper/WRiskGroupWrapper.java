package co.com.medplus.Medico.wrapper;

/**
 *
 * @author MarioF
 */
public class WRiskGroupWrapper {
   
    private int idRiskGroup;
    private String description;
    private String posible;
    private String confirm;
    private String codeDate;
    private String lender;

    /**
     * @return the idRiskGroup
     */
    public int getIdRiskGroup() {
        return idRiskGroup;
    }

    /**
     * @param idRiskGroup the idRiskGroup to set
     */
    public void setIdRiskGroup(int idRiskGroup) {
        this.idRiskGroup = idRiskGroup;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the posible
     */
    public String getPosible() {
        return posible;
    }

    /**
     * @param posible the posible to set
     */
    public void setPosible(String posible) {
        this.posible = posible;
    }

    /**
     * @return the confirm
     */
    public String getConfirm() {
        return confirm;
    }

    /**
     * @param confirm the confirm to set
     */
    public void setConfirm(String confirm) {
        this.confirm = confirm;
    }

    /**
     * @return the codeDate
     */
    public String getCodeDate() {
        return codeDate;
    }

    /**
     * @param codeDate the codeDate to set
     */
    public void setCodeDate(String codeDate) {
        this.codeDate = codeDate;
    }

    /**
     * @return the lender
     */
    public String getLender() {
        return lender;
    }

    /**
     * @param lender the lender to set
     */
    public void setLender(String lender) {
        this.lender = lender;
    }
    
    
}

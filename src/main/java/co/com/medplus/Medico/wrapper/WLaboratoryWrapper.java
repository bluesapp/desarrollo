package co.com.medplus.Medico.wrapper;

/**
 *
 * @author MarioF
 */
public class WLaboratoryWrapper {
    
    private int idLaboratoryAlert;
    private String code;
    private String laboratory;
    private String alert;
    private String result;
    private String comentary;
    private String dateAdmission;

    /**
     * @return the idLaboratoryAlert
     */
    public int getIdLaboratoryAlert() {
        return idLaboratoryAlert;
    }

    /**
     * @param idLaboratoryAlert the idLaboratoryAlert to set
     */
    public void setIdLaboratoryAlert(int idLaboratoryAlert) {
        this.idLaboratoryAlert = idLaboratoryAlert;
    }

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }

    /**
     * @return the laboratory
     */
    public String getLaboratory() {
        return laboratory;
    }

    /**
     * @param laboratory the laboratory to set
     */
    public void setLaboratory(String laboratory) {
        this.laboratory = laboratory;
    }

    /**
     * @return the alert
     */
    public String getAlert() {
        return alert;
    }

    /**
     * @param alert the alert to set
     */
    public void setAlert(String alert) {
        this.alert = alert;
    }

    /**
     * @return the result
     */
    public String getResult() {
        return result;
    }

    /**
     * @param result the result to set
     */
    public void setResult(String result) {
        this.result = result;
    }

    /**
     * @return the comentary
     */
    public String getComentary() {
        return comentary;
    }

    /**
     * @param comentary the comentary to set
     */
    public void setComentary(String comentary) {
        this.comentary = comentary;
    }

    /**
     * @return the dateAdmission
     */
    public String getDateAdmission() {
        return dateAdmission;
    }

    /**
     * @param dateAdmission the dateAdmission to set
     */
    public void setDateAdmission(String dateAdmission) {
        this.dateAdmission = dateAdmission;
    }
    
    
}
